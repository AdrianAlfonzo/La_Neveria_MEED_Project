    private void jTableProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductoMouseClicked
        int Indice = this.jTableProducto.getSelectedRow();
        DefaultTableModel mdlTmp = (DefaultTableModel) this.jTableProducto.getModel();

        txtProducto.setText(mdlTmp.getValueAt(Indice, 1).toString().trim());
        txtPrecio.setText(mdlTmp.getValueAt(Indice, 2).toString().trim());
        txtStock.setText(mdlTmp.getValueAt(Indice, 3).toString().trim());
        txtDescrip.setText(mdlTmp.getValueAt(Indice, 4).toString().trim());
    }//GEN-LAST:event_jTableProductoMouseClicked

