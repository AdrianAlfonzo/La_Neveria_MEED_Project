    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            int Indice= jTableTipoHelado.getSelectedRow();
            DefaultTableModel mdlTmp=(DefaultTableModel)jTableTipoHelado.getModel();
            connectObjSQL.ActualizarTipoHelado(jTableTipoHelado, this.txtTipoHelado.getText(), this.txtDescripcion.getText(), Integer.parseInt(mdlTmp.getValueAt(Indice, 0).toString()));
            this.UpdateTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar precisamente algún CAMPO en la TABLA");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

